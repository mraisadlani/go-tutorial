package main

import "fmt"

func main() {
	fmt.Println("Rais")
	fmt.Println("Muhammad Rais")
	fmt.Println("Muhammad Rais Adlani")

	// Menghitung jumlah karakter
	fmt.Println(len("Vanilla JS"))

	// Mengambil karakter pada posisi tertentu dengan dibaca kedalam per byte
	fmt.Println("Muhammad Rais"[0]) // M = 77
	fmt.Println("Muhammad Rais"[1]) // U = 117
}
